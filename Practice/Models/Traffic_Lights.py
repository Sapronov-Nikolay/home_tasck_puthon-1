# -*- coding: utf-8 -*-
"""
================================================================================
СВЕТОФОР С ПЕШЕХОДАМИ
================================================================================

ОГЛАВЛЕНИЕ (структура кода)
--------------------------------------------------------------------------------
1. КОНФИГУРАЦИЯ (пульт управления) – Тут все "КНОПКИ" конфигурации для управления
    1.1. Окно
    1.2. Автомобильный светофор (размеры, цвета, координаты)
    1.3. Пешеходный светофор
    1.4. ВременнЫе задержки и мигание перед переключением (в миллисекундах)
    1.5. Таймеры (отображение)
    1.6. Кнопки управления

2. КЛАСС TrafficLightWithPedestrian ДЛЯ КОПМЛЕКСА СВЕТОФОРОВ
    2.1 Инициализация конструктор (__init__)
        2.1.1. Создание окна
        2.1.2. Создание холста
        2.1.3. Инициализация переменных состояний
        2.1.4. Рисование автомобильного светофора
            2.1.4.1. Координаты центров ламп по вертикали
            2.1.4.2. Рисуем три овала (лампы автомобильного светофора)
            2.1.4.3. Таймер (цифры) над автомобильным светофором
        2.1.5. Рисование пешеходного светофора
            2.1.5.1. Координаты треугольников: верхний (красный), нижний (зелёный)
            2.1.5.2. Рисуем треугольники вспомогательным методом "def _create_triangle():" определён ниже (см. 2.2.1)
            2.1.5.3. Изначально оба треугольника-лампы выключены (серые)
            2.1.5.4. Таймер (текст) над пешеходным светофором
        2.1.6. Кнопки управления Пуск / Стоп
            2.1.6.1. Создаём кнопку "Пуск"
            2.1.6.2. Создаём кнопку "Стоп"
        2.1.7. Переменные для фаз и обратного отсчёта (тут их обнуляем)

    2.2. Метод рисования фигур
        2.2.1. _create_triangle() Создание треугольной лампы для пешеходного светофора

    2.3. Управление лампами автомобильного светофора
        2.3.1. _set_auto_lamps() Устанавливаем цвета, которыми будет пользоваться автомобильный светофор
        2.3.2. _auto_red() Включает только красный
        2.3.3. _auto_red_yellow() Включает красный + жёлтый
        2.3.4. _auto_green() Включает только зелёный
        2.3.5. _auto_yellow() Включает только жёлтый
        2.3.6. _auto_off() Выключает все лампы (серые, для кнопки "Стоп")

    2.4. Мигание зелёной лампы
        2.4.1. _start_blinking() Запускает мигание зелёной лампы
        2.4.2. _blink_step() Один шаг мигания: если горит – выключаем, иначе включаем
        2.4.3. _stop_blinking() Останавливает мигание, отменяет все таймеры мигания

    2.5. Управление лампами пешеходного светофора
        2.5.1. _set_ped_lamps() Устанавливает цвета двух треугольных ламп
        2.5.2. _ped_green() Включает зелёный треугольник, красный выключает
        2.5.3. _ped_red() Включает красный треугольник, зелёный выключает
        2.5.4. _ped_off() Выключает оба треугольника (серые, для кнопки "Стоп")

    2.6. Управление таймерами (цифровые отсчёты)
        2.6.1. _update_timers() Обновляет цифровые таймеры каждую секунду
            2.6.1.1. Обработка таймера автомобилей
            2.6.1.2. Обработка таймера пешеходов
            2.6.1.3. Уменьшаем счётчики (только для отображения)
            2.6.1.4. Запускаем следующее обновление через 1 секунду

    2.7. Основные фазы автомобильного светофора
        2.7.1. _phase_red() Фаза: только красный (авто) – 15 секунд
            2.6.1.1. Обработка таймера автомобилей
            2.6.1.2. Обработка таймера пешеходов
            2.6.1.3. Уменьшаем счётчики (только для отображения)
            2.6.1.4. Запускаем следующее обновление через 1 секунду

    2.7. Основные фазы автомобильного светофора
        2.7.1. _phase_red() Фаза: только красный (авто) - 15 секунд. Пешеходный – зелёный.
        2.7.2. _phase_red_yellow() Фаза: красный+жёлтый (авто) – 1 секунда. Пешеходный красный.
        2.7.3. _phase_green() Фаза: только зелёный (авто) – 15 секунд. Запускается мигание
        2.7.4. _phase_yellow() Фаза: только жёлтый (авто) – 2 секунды. Останавливаем мигание

    2.8. Пользовательские методы запуска / остановки
        2.8.1. start() Запуск цикла светофора
        2.8.2. stop() Остановка цикла, отмена всех таймеров, выключение всех ламп, сброс таймеров.

3. ЗАПУСК ПРОГРАММЫ
--------------------------------------------------------------------------------
"""

import tkinter as tk    # Импортируем tkinter (сокращённо tk) для графики


# ============================================================
# 1. КОНФИГУРАЦИЯ (пульт управления) – Тут все "КНОПКИ" конфигурации для управления
# ============================================================

# ----------------------------------------------------------------------------
# 1.1. Окно
# ----------------------------------------------------------------------------
WINDOW_TITLE = "Светофор автомобильно-пешеходный"
WINDOW_WIDTH = 700                      # ширина окна в пикселях
WINDOW_HEIGHT = 600                     # высота окна в пикселях
CANVAS_WIDTH = 680                      # ширина холста (поля для рисования)
CANVAS_HEIGHT = 500                     # высота холста
CANVAS_BG = "#2e2e2e"                   # цвет фона холста (тёмный, как асфальт)
MAIN_FRAME_PADY = 20                    # отступа холста от верхнего края

# ----------------------------------------------------------------------------
# 1.2. Автомобильный светофор
# ----------------------------------------------------------------------------
AUTO_X = 100                            # координата X центра светофора (в пикселях)
AUTO_Y = 200                            # координата Y центра светофора (в пикселях)
AUTO_CORPUS_COLOR = "#333333"           # цвет корпуса светофора (тёмно-серый)
AUTO_CORPUS_WIDTH = 100                 # ширина корпуса светофора
AUTO_CORPUS_HEIGHT = 250                # высота корпуса светофора
LAMP_W = 70                             # ширина лампы светофора (овал)
LAMP_H = 55                             # высота лампы светофора (овал)
# Цвета ламп:
LAMP_OUTLINE = "black"                  # цвет обводки ламп светофора
AUTO_RED_COLOR = "red"                  # цвет красной лампы светофора
AUTO_YELLOW_COLOR = "yellow"            # цвет жёлтой лампы светофора
AUTO_GREEN_COLOR = "green"              # цвет зелёной лампы светофора
OFF_COLOR = "gray"                      # цвет ламп в выключенном состоянии

# ----------------------------------------------------------------------------
# 1.3. Пешеходный светофор
# ----------------------------------------------------------------------------
PED_X = AUTO_X + 300                    # X центра пешеходного светофора (правее автомобильного)
PED_Y = AUTO_Y                          # Y центра пешеходного светофора (такой же, как у автомобильного)
PED_CORPUS_COLOR = "#333333"            # цвет корпуса пешеходного светофора
PED_CORPUS_WIDTH = 60                   # высота корпуса пешеходного светофора
PED_CORPUS_HEIGHT = 160                 # ширина корпуса пешеходного светофора
TRI_SIZE = 30                           # размер стороны треугольной лампы пешеходного светофора
PED_RED_COLOR = "red"                   # цвет красной треугольной лампы
PED_GREEN_COLOR = "green"               # цвет зелёной треугольной лампы
PED_OFF_COLOR = OFF_COLOR               # цвет треугольной лампы в выключенном состоянии

# ----------------------------------------------------------------------------
# 1.4. Временные задержки и мигание (в миллисекундах)
# ----------------------------------------------------------------------------
AUTO_RED_DELAY = 15000                  # красный горит 15 секунд
AUTO_RED_YELLOW_DELAY = 2000            # красный+жёлтый горит 1 секунду
AUTO_GREEN_DELAY = 15000                # зелёный горит 15 секунд
AUTO_YELLOW_DELAY = 2000                # жёлтый горит 2 секунды
# фазы мигания
BLINK_DURATION = 3000                   # за сколько миллисекунд до конца фазы начинает мигать (3 сек)
BLINK_INTERVAL = 500                    # интервал мигания (0.5 сек) - вкл/выкл
# Задержка безопасности: пешеходный становится красным за 4 сек до подачи зелёного машинам
PED_SAFETY_DELAY = 4000                 # (реализовано через переключение в фазе красный+жёлтый)

# ----------------------------------------------------------------------------
# 1.5. Таймеры (отображение)
# ----------------------------------------------------------------------------
TIMER_FONT = ("Arial", 14, "bold")      # шрифт таймеров
TIMER_BG = "black"                      # фон таймера (пока не используется)
TIMER_FG_AUTO = "white"                 # цвет текста таймера для автомобилей
TIMER_FG_PED = "white"                  # цвет текста таймера для пешеходов
TIMER_UPDATE_MS = 1000                  # обновление таймера каждую секунду (1000 мс)

# ----------------------------------------------------------------------------
# 1.6. Кнопки управления
# ----------------------------------------------------------------------------
BUTTON_FRAME_PADY = 10                  # горизонтальный отступ фрейма с кнопками
BUTTON_SIDE = tk.LEFT                   # распаковать кнопки в ряд слева на право
BUTTON_PADX = 5                         # отступ между кнопками распакованными в ряд
# ----------------------------------------------------------------------------

# ============================================================================
# 2. КЛАСС TrafficLightWithPedestrian ДЛЯ КОПМЛЕКСА СВЕТОФОРОВ
# ============================================================================
class TrafficLightWithPedestrian:
    """ Главный класс, обновляющий автомобильный и пешеходный светофоры."""
    # ----------------------------------------------------------------------------
    # 2.1 Инициализация конструктор (__init__)
    # ----------------------------------------------------------------------------
    def __init__(self, root):
        """
            Конструктор: создаёт окно, холст, рисует корпуса, лампы, таймеры, настраивает кнопки и начальные состояния
        """
        self.root = root

        # 2.1.1. Создание окна
        root.title(WINDOW_TITLE)
        root.geometry(f"{WINDOW_WIDTH}x{WINDOW_HEIGHT}")

        # 2.1.2. Создание холста
        # Холст - это область, на которой мы рисуем все фигуры.
        self.canvas = tk.Canvas(
            root,
            width=CANVAS_WIDTH,
            height=CANVAS_HEIGHT,
            bg=CANVAS_BG
        )
        self.canvas.pack(pady=MAIN_FRAME_PADY)   # размещаем холст в окне с отступом сверху/снизу

        # 2.1.3. Инициализация переменных состояний
        self.running = False                # запущен ли цикл переключения
        self.after_id = None                # ID текущего таймера (для отмены)
        self.timer_after_id = None          # ID таймера обновления цифр
        self.blink_after_id = None          # ID таймера мигания (если активно)
        self.blink_start_id = None          # ID тфймера, запланировавшего начало мигания
        self.ped_green_after_id = None      # ID таймера задержки включения зелёного для пешеходов

        # 2.1.4. Рисование автомобильного светофора
        # Рисуем корпус (прямоугольник) с центром в (AUTO_X, AUTO_Y)
        # правильный расчёт координат: от центра отступаем половину ширины/высоты
        self.auto_corpus = self.canvas.create_rectangle(
            AUTO_X - AUTO_CORPUS_WIDTH // 2,          # x1 = центр - половина ширины
            AUTO_Y - AUTO_CORPUS_HEIGHT // 2,         # y1 = центр - половина высоты
            AUTO_X + AUTO_CORPUS_WIDTH // 2,          # x2 = центр + половина ширины
            AUTO_Y + AUTO_CORPUS_HEIGHT // 2,         # y2 = центр + половина высоты
            fill=AUTO_CORPUS_COLOR,
            outline="black",
            width=2
        )

        # 2.1.4.1. Координаты центров ламп по вертикали
        # Красная лампа: выше центра на четверть высоты корпуса
        red_y = AUTO_Y - AUTO_CORPUS_HEIGHT // 4      # выше центра на четверть высоты 200 - 250//4 = 200 - 62 = 138
        # Жёлтая лампа по центру
        yellow_y = AUTO_Y                           # по центру 200
        # Зелёная лампа: ниже центра на четверть высоты корпуса
        green_y = AUTO_Y + AUTO_CORPUS_HEIGHT // 4    # ниже центра на четверть 200 + 250//4 = 200 + 62 = 262

        # 2.1.4.2. Рисуем три овала (лампы автомобильного светофора)
        # Красная лампа (овал)
        self.auto_red = self.canvas.create_oval(
            AUTO_X - LAMP_W // 2,         # x1 = 65: левый край (100 - 70//2 = 100 - 35)
            red_y - LAMP_H // 2,          # y1 = 111: верхний край (138 - 55//2 = 138 - 27)

            AUTO_X + LAMP_W // 2,         # x2 = 135: правый край (100 + 70//2 = 100 + 35)
            red_y + LAMP_H // 2,          # y2 = 165: нижний край (138 + 55//2 = 138 + 27)
            fill=OFF_COLOR,
            outline=LAMP_OUTLINE
        )
        # Жёлтая лампа (овал)
        self.auto_yellow = self.canvas.create_oval(
            AUTO_X - LAMP_W // 2,         # x1 = 65: левый край (100 - 70//2 = 100 - 35)
            yellow_y - LAMP_H // 2,       # y1 = 173: верхний край (200 - 55//2 = 200 - 27)

            AUTO_X + LAMP_W // 2,         # x2 = 135: правый край (100 + 70//2 = 100 + 35)
            yellow_y + LAMP_H // 2,       # y2 = 227: нижний край (200 + 55//2 = 200 + 27)
            fill=OFF_COLOR,
            outline=LAMP_OUTLINE
        )
        # Зелёная лампа (овал)
        self.auto_green = self.canvas.create_oval(
            AUTO_X - LAMP_W // 2,         # x1 = 65: левый край (100 - 70//2 = 100 - 35)
            green_y - LAMP_H // 2,        # y1 = 235: верхний край (262 - 55//2 = 262 - 27)

            AUTO_X + LAMP_W // 2,         # x2 = 135: правый край (100 + 70//2 = 100 + 35)
            green_y + LAMP_H // 2,        # y2 = 289: нижний край (262 + 55//2 = 262 + 27)
            fill=OFF_COLOR,
            outline=LAMP_OUTLINE
        )

        # 2.1.4.3. Таймер (цифры) над автомобильным светофором
        self.auto_timer_text = self.canvas.create_text(
            AUTO_X,                                 # x = 100: по центру светофора (совпадает с AUTO_X)
            AUTO_Y - AUTO_CORPUS_HEIGHT // 2 - 20,  # y = 55: над корпусом светофора
            text="00:00",                           # начальный текст таймера (формат ММ:СС)
            font=TIMER_FONT,                        # шрифт: ("Arial", 14, "bold")
            fill=TIMER_FG_AUTO                      # цвет текста: белый (TIMER_FG_AUTO = "white")
        )

        # 2.1.5. Рисование пешеходного светофора
        self.ped_corpus = self.canvas.create_rectangle(
            PED_X - PED_CORPUS_WIDTH // 2,            # x1: левый верхний угол расчёт: 300 - 60//2 = 300 - 30 = 270
            PED_Y - PED_CORPUS_HEIGHT // 2,           # y1: верхний край корпуса расчёт: 400 - 160//2 = 400 - 80 = 320
            PED_X + PED_CORPUS_WIDTH // 2,            # x2: правый нижний угол расчёт: 300 + 60//2 = 300 + 30 = 330
            PED_Y + PED_CORPUS_HEIGHT // 2,           # y2: нижний край корпуса расчёт: 400 + 160//2 = 400 + 80 = 480
            fill=PED_CORPUS_COLOR,                  # цвет заливки корпуса
            outline="black",                        # цвет контура
            width=2                                 # толщина контура
        )

        # 2.1.5.1. Координаты треугольников: верхний (красный), нижний (зелёный)
        tri_top_y = PED_Y - PED_CORPUS_HEIGHT // 4
        tri_bottom_y = PED_Y + PED_CORPUS_HEIGHT // 4

        # 2.1.5.2. Рисуем треугольники вспомогательным методом "def _create_triangle():" определён ниже (см. 2.2.1)
        self.ped_red = self._create_triangle(PED_X, tri_top_y, PED_RED_COLOR)
        self.ped_green = self._create_triangle(PED_X, tri_bottom_y, PED_GREEN_COLOR)

        # 2.1.5.3. Изначально оба треугольника-лампы выключены (серые)
        self.canvas.itemconfig(self.ped_red, fill=OFF_COLOR)
        self.canvas.itemconfig(self.ped_green, fill=OFF_COLOR)

        # 2.1.5.4. Таймер (текст) над пешеходным светофором
        self.ped_timer_text = self.canvas.create_text(
            PED_X,                                  # x = 100: по центру светофора (совпадает с AUTO_X)
            PED_Y - PED_CORPUS_HEIGHT // 2 - 20,    # y = 55: над корпусом светофора
            text="00:00",                           # начальный текст таймера (формат ММ:СС)
            font=TIMER_FONT,                        # шрифт: ("Arial", 14, "bold")
            fill=TIMER_FG_PED                       # цвет текста: белый (TIMER_FG_AUTO = "white")
        )

        # 2.1.6. Кнопки управления Пуск / Стоп
        btn_frame = tk.Frame(root)              # создаём контейнер (фрейм) для размещения кнопок
        btn_frame.pack(
            pady=BUTTON_FRAME_PADY              # размещаем фрейм в окне с вертикальным отступом
        )

        # 2.1.6.1 Создаём кнопку "Пуск"
        self.start_btn = tk.Button(         # инициализируем объект: первую кнопку
            btn_frame,                      # родительнский контейнер - ранее созданный фрейм btn_frame
            text="Пуск",                    # текст, отображаемый на кнопке
            command=self.start              # метод, вызываемый при нажатии (ссылка на метод self.start)
        )
        self.start_btn.pack(                # позиционируем - размещаем кнопку внутри фрейма
            side=BUTTON_SIDE,               # сторона размещения кнопки слева на право (см. 1.6.)
            padx=BUTTON_FRAME_PADY          # вертикальный отступ от краёв нижних и верхних
        )

        # 2.1.6.2. Создаём кнопку "Стоп"
        self.stop_btn = tk.Button(          # инициализируем объект: вторую кнопку
            btn_frame,                      # родительнский контейнер - ранее созданный фрейм btn_frame
            text="Стоп",                    # текст, отображаемый на кнопке
            command=self.stop               # метод, выполняемый при клике (ссылка на метод self.stop)
        )
        self.stop_btn.pack(                 # позиционируем - размещаем кнопку внутри фрейма
            side=BUTTON_SIDE,               # сторона размещения кнопки слева на право (см. 1.6.)
            padx=BUTTON_PADX                # горизонтальный отступ по бокам обеспечивает отступы между кнопками
        )

        # 2.1.7. Переменные для фаз и обратного отсчёта (тут их обнуляем)
        self.current_phase = None           # "red", "red_yellow", "green", "yellow"
        self.phase_remaining = 0            # оставшиеся секунды текущей фазы (для авто)
        self.ped_phase = None               # "green" или "red"
        self.ped_remaining = 0              # оставшиеся секунды для пешеходов
        self.is_blinking = False            # флаг: мигает ли зелёный в данный момент

    # ----------------------------------------------------------------------------
    # 2.2. _create_triangle() – Метод рисования фигур
    # ----------------------------------------------------------------------------
    def _create_triangle(self, cx, cy, color):
        """
        Рисует равнобедренный треугольник (вершиной верх).
        cx, cy - центр треугольника  (координаты на холсте).
        color – цвет заливки
        Возвращает ID фигуры на холсте.
        """

        # 2.2.1. Создание треугольной лампы для пешеходного светофора
        half = TRI_SIZE // 2    # Вычисляем половину размера треугольника
        # Координаты трёх точек: левый нижний, правый нижний, верхняя вершина
        # Формат: [x1, y1, x2, y2, x3, y3]
        # где:
        """
            (x1, y1) - левый нижний угол
            (x2, y2) - правый нижний угол
            (x3, y3) - верхняя вершина
        """
        points = [
            cx - half, cy + half,   # левый нижний угол
            cx + half, cy + half,   # правый нижний угол
            cx, cy - half           # верхняя вершина
        ]
        # create_polygon рисует многоугольник по списку точек
        # Параметр color является динамическим и может принимать разные значения в зависимости:
        # текущего состояния светофора, фазы переключения, режима работы
        return self.canvas.create_polygon(
            points,             # координаты вершин треугольника из списка points[]
            fill=color,         # заливка фигуры текущим цветом сигнала
            outline="black"     # черный контур для четкости отображения
        )

    # ----------------------------------------------------------------------------
    # 2.3. Управление лампами автомобильного светофора
    # ----------------------------------------------------------------------------
    def _set_auto_lamps(self, red, yellow, green):
        """
        Устанавливает цвета трёх ламп автомобильного светофора
        Метод централизованно управляет визуальным состоянием сигналов:
        принимает три параметра (цвета для красного, жёлтого и зелёного),
        обновляет соответствующие графические объекты на холсте.
        """

        # 2.3.1. Устанавливаем цвета, которыми будет пользоваться автомобильный светофор
        # По сути мы даём устройству возможность пользоваться тем, что тут создаём
        # Красный свет светофора
        self.canvas.itemconfig(     # Устанавливаем цвет красной лампы
            self.auto_red,          # идентификатор объекта лампы на canvas
            fill=red                # задаёт заливку: определяет активность сигнала
        )
        # Жёлтый свет светофора
        self.canvas.itemconfig(     # Устанавливаем цвет жёлтой лампы
            self.auto_yellow,       # идентификатор жёлтой лампы на canvas
            fill=yellow             # задаёт заливку: определяет активность сигнала
        )
        # Зелёный свет светофора
        self.canvas.itemconfig(     # Устанавливаем цвет зелёной лампы
            self.auto_green,        # идентификатор зелёной лампы на canvas
            fill=green              # задаёт заливку: определяет активность сигнала
        )

    def _auto_red(self):

        # 2.3.2. Включает только красный
        self._set_auto_lamps(       # светофорное табло: стоим на месте
            AUTO_RED_COLOR,         # зажигает красный светофор
            OFF_COLOR,              # жёлтый не горит
            OFF_COLOR               # зелёный не горит
        )

    def _auto_red_yellow(self):

        # 2.3.3. Включает красный + жёлтый
        self._set_auto_lamps(       # светофорное табло: готовимся ехать
            AUTO_RED_COLOR,         # горит красный
            AUTO_YELLOW_COLOR,      # одновременно загорелся жёлтый
            OFF_COLOR               # зелёный ещё не горит
        )

    def _auto_green(self):

        # 2.3.4. Включает только зелёный
        self._set_auto_lamps(       # светофорное табло: поехали!
            OFF_COLOR,              # уже не горит
            OFF_COLOR,              # уже не горит
            AUTO_GREEN_COLOR        # загорелся зелёный
        )

    def _auto_yellow(self):

        # 2.3.5. Включает только жёлтый
        self._set_auto_lamps(       # светофорное табло: ВНИМАНИЕ! скоро красный
            OFF_COLOR,              # красный ещё не горит
            AUTO_YELLOW_COLOR,      # жёлтый уже горит
            OFF_COLOR               # зелёный уже не горит
        )

    def _auto_off(self):

        # 2.3.6. Выключает все лампы (серые, для кнопки "Стоп")
        self._set_auto_lamps(       # светофорное табло: отключено при нажатии на кнопку "Стоп"
            OFF_COLOR,              # не горит
            OFF_COLOR,              # не горит
            OFF_COLOR               # не горит
        )

    # ----------------------------------------------------------------------------
    # 2.4. Мигание зелёной лампы
    # ----------------------------------------------------------------------------
    def _start_blinking(self):

        # 2.4.1. Запускает мигание зелёной лампы.
        # Вызывается за BLINK_DURATION - (за 3 сек) до окончания зелёной фазы.
        if not self.running:                            # если нет активности светофора
            return                                      # завершаем эту функцию и идём дальше
        self.is_blinking = True                         # активируем режим мигания, устанавливаем флаг мигания
        self._blink_step()                              # запускаем первый шаг мигания

    def _blink_step(self):                              # выполняет один цикл мигания лампы

        # 2.4.2. Один шаг мигания: если горит – выключаем, иначе включаем.
        # Планирует следующий шаг через BLINK_INTERVAL (интервал мигания = 0.5 сек).
        if not self.running or not self.is_blinking:    # если цикл мигания и не мигает зелёный свет
            return                                      # функция завершается без выполнения дальнейших действий
        # Получаем текущий цвет лампы - условие, что она зелёная (светится)
        current = self.canvas.itemcget(                 # получаем текущий цвет лампы - значение параметра fill
            self.auto_green,                            # даём fill цвет зелёной лампы
            "fill"                                # проверяем параметр, который принял значение чуть ранее
        )
        # Меняем состояние лампы на противоположное
        if current == AUTO_GREEN_COLOR:                 # если лампа сейчас зелёная
            self.canvas.itemconfig(                     # меняем параметры объекта
                self.auto_green,                        # на объект зелёной лампы
                fill=OFF_COLOR                          # заполняем - устанавливаем цвет выключенной лампы
        )
        else:                                           # иначе если лампа сейчас выключена
            self.canvas.itemconfig(                     # восстанавливаем параметры
                self.auto_green,                        # задаём объекту цвет зелёной лампы
                fill=AUTO_GREEN_COLOR                   # заполняем - устанавливаем зелёный цвет
        )
        # Планируем следующий шаг мигания
        self.blink_after_id = self.root.after(          # запланируем следующее выполнение
            BLINK_INTERVAL,                             # через интервал BLINK_INTERVAL (0.5 сек)
            self._blink_step                            # перезапускаем эту же функцию
        )

    def _stop_blinking(self):

        # 2.4.3. Останавливает мигание, отменяет все таймеры мигания.
        # Восстанавливает зелёный, как постоянно горязий.
        if self.blink_after_id:                         # проверяем и отменяем активный таймер мигания
            self.root.after_cancel(
                self.blink_after_id                     # отменяем запланированное мигание
            )
            self.blink_after_id = None                  # обнуляем идентификатор таймера если он сейчас был не None
        if self.blink_start_id:                         # проверяем и отменяем таймер начала мигания
            self.root.after_cancel(
                self.blink_start_id                     # отменяем отложенное начало
            )
            self.blink_start_id = None                  # обнуляем идентификатор
        self.is_blinking = False                        # отключаем режим мигания ставим флаг False
        self.canvas.itemconfig(                         # восстанавливаем постоянный зелёный цвет
            self.auto_green,                            # задаём объекту цвет зелёной лампы
            fill=AUTO_GREEN_COLOR                       # заполняем - устанавливаем зелёный цвет
        )

    # ----------------------------------------------------------------------------
    # 2.5. Управление лампами пешеходного светофора
    # ----------------------------------------------------------------------------
    def _set_ped_lamps(self, red, green):

        # 2.5.1. Устанавливает цвета двух треугольных ламп
        self.canvas.itemconfig(                 # Устанавливаем цвет красной лампы
            self.ped_red,                       # идентификатор объекта лампы на canvas
            fill=red                            # задаёт заливку: определяет активность сигнала
        )
        self.canvas.itemconfig(                 # Устанавливаем цвет зелёной лампы
            self.ped_green,                     # идентификатор объекта лампы на canvas
            fill=green                          # задаёт заливку: определяет активность сигнала
        )

    def _ped_green(self):

        # 2.5.2. Включает зелёный треугольник, красный выключает
        self._set_ped_lamps(                     # светофорное табло: пошли!
            OFF_COLOR,                          # красный не горит
            PED_GREEN_COLOR                     # зелёный горит
        )

    def _ped_red(self):

        # 2.5.3. Включает красный треугольник, зелёный выключает
        self._set_ped_lamps(                    # светофорное табло: стоим!
            PED_RED_COLOR,                      # загорается красный
            OFF_COLOR                           # погас зелёный
        )

    def _ped_off(self):

        # 2.5.4. Выключает оба треугольника (серые, для кнопки "Стоп")
        self._set_ped_lamps(                    # светофорное табло: отключено при нажатии на кнопку "Стоп"
            OFF_COLOR,                          # не горит
            OFF_COLOR                           # не горит
        )

    # ----------------------------------------------------------------------------
    # 2.6. Управление таймерами (цифровые отсчёты)
    # ----------------------------------------------------------------------------
    def _update_timers(self):

        # 2.6.1. Обновляет цифровые таймеры каждую секунду.
        # Уменьшает phase_remaining и ped_remaining на 1.
        if not self.running:                            # если нет работы _update_timers
            return                                      # то прекращаем выполнение функции и идём дальше

        # 2.6.1.1. Обработка таймера автомобилей
        # Вычисляем минуты и секунды из оставшегося времени
        auto_min = max(0, self.phase_remaining) // 60    # минуты
        auto_sec = max(0, self.phase_remaining) % 60    # секунды
        auto_text = f"{auto_min:02d}:{auto_sec:02d}"    # формируем строку с ведущими нулями
        self.canvas.itemconfig(                         # обновляем текст на canvas
            self.auto_timer_text,                       # объект текстового поля
            text=auto_text                              # устанавливаем новый текст
        )

        # 2.6.1.2. Обработка таймера пешеходов
        ped_min = max(0, self.ped_remaining) // 60      # минуты
        ped_sec = max(0, self.ped_remaining) % 60       # секунды
        ped_text = f"{ped_min:02d}:{ped_sec:02d}"       # формируем строку с ведущими нулями
        self.canvas.itemconfig(                         # обновляем текст на canvas
            self.ped_timer_text,                        # объект текстового поля
            text=ped_text                               # устанавливаем новый текст
        )

        # 2.6.1.3. Уменьшаем счётчики (только для отображения)
        # Проверяем, что время ещё не истекло
        if self.phase_remaining > 0:                    # для автомобильного таймера
            self.phase_remaining -= 1                   # уменьшаем на 1 секунду
        if self.ped_remaining > 0:                      # для пешеходного таймера
            self.ped_remaining -= 1                     # уменьшаем на 1 секунду

        # 2.6.1.4. Запускаем следующие обновления через 1 секунду
        self.timer_after_id = self.root.after(          # запускаем таймер
            TIMER_UPDATE_MS,                            # интервал обновления (в миллисекундах)
            self._update_timers                         # функция для повторного вызова
        )

    # ----------------------------------------------------------------------------
    # 2.7. Основные фазы автомобильного светофора
    # ----------------------------------------------------------------------------
    def _phase_red(self):

        # 2.7.1. Фаза: только красный (авто) - 15 секунд. Пешеходный – зелёный.
        # Пешеходный - зелёный.
        if not self.running:                                # если не активен
            return                                          # выходим из функции
        # Установка текущей фазы
        self.current_phase = "red"                          # запоминаем текущую фазу
        self.phase_remaining = AUTO_RED_DELAY // 1000       # переводим миллисекунды в секунды 15000 мс / 1000 = 15 секунд
        self._auto_red()                                    # по истечении 15 сек включаем свет из текущей фазы
        # Настройка пешеходной фазы
        self.ped_phase = "green"                            # пешеходный зелёный
        self.ped_remaining = self.phase_remaining           # ставим уже установленное для авто время (15 сек)
        # Включаем зелёный для пешеходов с задержкой 1 секунда
        self.ped_green_after_id = self.root.after(          # планируем задержку перед переключением
            1000,                                           # задержка в 1 сек
            self._ped_green                                 # запускаем зелёный пешеходам
        )
        self.ped_green_after_id = self.root.after(          # планируем переход к следующей фазе
            AUTO_RED_DELAY,                                 # выбираем цвет
            self._phase_red_yellow                          # включаем красный=жёлтый
        )

    def _phase_red_yellow(self):

        # 2.7.2. Фаза: красный+жёлтый (авто) – 1 секунда. Пешеходный красный.
        # Пешеходный переключается на красный (безопасность).
        if not self.running:                                    # если не активен
            return                                              # выходим из функции
        # Настройка автомобильной фазы
        self.current_phase = "red_yellow"                       # запоминаем текущую фазу
        self.phase_remaining = AUTO_RED_YELLOW_DELAY // 1000    # переводим миллисекунды в секунды 15000 мс / 1000 = 15 секунд
        self._auto_red_yellow()                                 # включаем красно-жёлтый сигнал
        # Настройка пешеходной фазы
        self.ped_phase = "red"                                  # задаём цвет для пешеходов - красный
        ped_red_seconds = (AUTO_RED_YELLOW_DELAY + AUTO_GREEN_DELAY + AUTO_YELLOW_DELAY) // 1000 # 1 + 15 + 2 = 18 сек
        self.ped_remaining = ped_red_seconds                    # устанавливаем переведённые 18000 мс в 16 сек
        self._ped_red()                                         # включаем красный для пешеходов
        # Планируем переход к зелёной фазе
        self.after_id = self.root.after(                        # запланировали следующее действие
            AUTO_RED_YELLOW_DELAY,                              # через 1 сек (1000 мс)
            self._phase_green                                   # переходим к следующей фазе (функции)
        )

    def _phase_green(self):

        # 2.7.3. Фаза: толькл зелёный (авто) - 15 секунд. Запускается мигание
        if not self.running:                                    # если не активен
            return                                              # выходим из функции

        # Настройка автомобильной фазы
        self.current_phase = "green"                            # запоминаем текущую фазу
        self.phase_remaining = AUTO_GREEN_DELAY // 1000         # переводим миллисекунды в секунды 15000 мс / 1000 = 15 секунд
        self._auto_green()                                      # включаем залёный сигнал
        # Планируем начало мигания
        blink_start_delay = AUTO_GREEN_DELAY - BLINK_DURATION   # время до начала мигания
        if blink_start_delay > 0:                               # если время положительное
            self.blink_start_id = self.root.after(
                blink_start_delay,                              # через заданное время (12 сек)
                self._start_blinking                            # включаем мигание
            )
        else:                                                   # иначе если время отрицательное или нулевое
            self._start_blinking()                              # запускаем мигание немедленно
        # Планируем переход к зелёной фазе
        self.after_id = self.root.after(                        # запланировали следующее действие
            AUTO_GREEN_DELAY,                                   # через 15 сек (15000 мс)
            self._phase_yellow                                  # переходим к жёлтой фазе
        )

    def _phase_yellow(self):

        # 2.7.4. Фаза: только жёлтый (авто) – 2 секунды. Останавливаем мигание
        if not self.running:                                    # если не активен
            return                                              # выходим из функции
        self._stop_blinking()                                   # останавливаем процесс мигания
        # Устанавливаем параметры жёлтой фазы
        self.current_phase = "yellow"                           # запоминаем текущую фазу
        self.phase_remaining = AUTO_YELLOW_DELAY // 1000        # переводим 2000 мс в 2 сек
        self._auto_yellow()                                     # включаем жёлтый свет
        # Пешеходный таймер остаётся без изменений
        # Планируем переход к следующей фазе
        self.after_id = self.root.after(                        # запланировали следующее действие
            AUTO_YELLOW_DELAY,                                  # через 2 сек (2000 мс)
            self._phase_red                                     # переходим к красной фазе
        )

    # ----------------------------------------------------------------------------
    # 2.8. Пользовательские методы запуска / остановки
    # ----------------------------------------------------------------------------
    def start(self):

        # 2.8.1. Запуск цикла светофора
        # Если уже запущен - ничего не делает
        # Проверяем, работает ли светофор
        if self.running:                            # если он уже и так запущен
            return                                  # ничего не делаем он и так работает
        # Инициализируем запуск
        self.running = True                         # устанавливаем флаг работы
        self._phase_red()                           # начинаем с красной фазы
        self._update_timers()                       # запускаем обновление таймеров

    def stop(self):

        # 2.8.2. Остановка цикла, отмена всех таймеров, выключение всех ламп, сброс таймеров.
        # Останавливаем работу
        self.running = False                        # отключаем флаг работы
        # отменяем активные таймеры
        if self.after_id:                           # если есть основной таймер
            self.root.after_cancel(self.after_id)   # отменяем его
            self.after_id = None                    # очищаем ссылку
        # Отменяем таймер обновления цифровых таймеров (если он есть)
        if self.timer_after_id:                     #
            self.root.after_cancel(self.timer_after_id) #
            self.timer_after_id = None              #
        # Отменяем таймер задержки включения зелёного для пешеходов (добавлено)
        if self.ped_green_after_id:
            self.root.after_cancel(self.ped_green_after_id)
            self.ped_green_after_id = None
        # Отключаем все процессы
        self._stop_blinking()                       # останавливаем мигание
        self._auto_off()                            # выключаем автомобильные лампы
        self._ped_off()                             # выключаем пешеходные лампы
        # Обнуляем таймеры
        self.canvas.itemconfig(                     # обновляем текс таймера авто
            self.auto_timer_text,                   # берём объект таймера
            text="00:00"                            # устанавливаем нулевое значение
        )
        self.canvas.itemconfig(                     # обновляем текс таймера авто
            self.ped_timer_text,                    # берём объект таймера
            text="00:00"                            # устанавливаем нулевое значение
        )
        # Сбрасываем все состояния
        self.phase_remaining = 0                    # обнуляем оставшееся времяфазы
        self.ped_remaining = 0                      # обнуляем оставшееся время для пешеходов
        self.current_phase = None                   # очищаем текущую фазу
        self.ped_phase = None                       # очищаем фазу для пешеходов
        self.is_blinking = False                    # отключаем режим мигания

