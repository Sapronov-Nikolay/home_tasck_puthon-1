# Условие перезаписи переменной соблюдено.

# Импортируем класс date из модуля datetime чтоб с датами работать
from datetime import date, datetime

def decliner_year (number):
  """
  Склонение слова "год" в зависимости от числа.
  Возвращает правильную форму: "год", "года", "лет".
  """
  last_two = number % 100   # Получаем последние две последние цифры числа 11-19
  last_one = number % 10    # Получаем последнюю цифру числа 1-9

  # Если число оканчивается на с 11 по 19 - используем слово "лет"
  if 11 <= last_two <= 19:
    return "лет"
  # Если последняя цифра - 1 (но не из диапазона 11-19), используем "год"
  if last_one == 1:
    return "год"
  # Если последняя цифра 2, 3 и 4 - используем "года"
  if 2 <= last_one <= 4:
    return "года"
  # Во всех остальных случаях - "лет" (например, для 0, 5-9)
  return "лет"

# Словарь для перевода номеров месяцев в их названия на русском языке
months_ru = {
  1: 'января',
  2: 'февраля',
  3: 'марта',
  4: 'апреля',
  5: 'мая',
  6: 'июня',
  7: 'июля',
  8: 'августа',
  9: 'сентября',
  10: 'октября',
  11: 'ноября',
  12: 'декабря'
}

# --- ОПРЕДЕЛЯЕМ ФОРМАТ ВВОДА ---
# Указываем дату рождения (ДД-ММ-ГГГГ или ГГГГ-ММ-ДД) в российском стандарте
while True:
  birth_date_str = input("Введите дату рождения в форматах - ДД-ММ-ГГГГ или ГГГГ-ММ-ДД: ")
  try:
    # Сначала попробуем распознать как ДД-ММ-ГГГГ
    birth_date = datetime.strptime(birth_date_str, "%d-%m-%Y"). date()
    break # Если успешно, выходим из цикла
  except ValueError:
    try:
      # Если не получилось, пробуем формат ГГГГ-ММ-ДД
      birth_date = datetime.strptime(birth_date_str, "%Y-%m-%d").date()
      break
    except ValueError:
      # Если оба формата не подошли - сообщаем об ошибке и просим повторить
      print("Неверный формат или не корректная дата. Используйте ДД-ММ-ГГГГ или ГГГГ-ММ-ДД")
# Конец ввода


# Получаем текущую дату (сегодня)
today = date.today()

# Вычисляем возраст: Разница между сегодня и годом рождения
age = today.year - birth_date.year
# Корректируем возраст: если день рожденияещё не наступил, отнимаем 1 год
if (today.month, today.day) < (birth_date.month, birth_date.day):
  age -= 1

my_age = age        # Сохраняем текущий возраст в my_age
my_age = my_age + 3 # Перезаписываем переменную. Теперь это текущий возраст с прибавкой 3

# Определяем склонение слова "год" для нового возраста
year_word = decliner_year(my_age)

# Получаем название текущего месяца из словаря months_ru
month_name = months_ru[today.month]

# Выводим текущую дату в формате "Сегодня [день] [месяц] [год] года" или наоборот если так ввели
print(f"Сегодня {today.day} {month_name} {today.year} года.")

# Выводим сообщение о возрасте через три года: Через три года вам будет [возраст] [форма слова год].
print(f"Через три года вам будет {my_age} {year_word}.")